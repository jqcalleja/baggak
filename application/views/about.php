                        <p class="text-center fs-4">“BAGGAK” is the Iloko term for Morning Star, the first to shine at dawn before the coming out of the sun from the east. The term “BAGGAK” was taken as a way of commemorating one of the most observed characteristics of the owner’s father that is being an early riser and being an industrious person who worked as early as dawn. Son’s was taken after the four sons of the owner. This was how the name of the establishment, BAGGAK SON’Z Hotel and Restaurant was coined.</p>
